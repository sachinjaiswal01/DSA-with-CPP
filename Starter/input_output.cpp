#include<iostream>
using namespace std;
int main()
{
    int n1,n2;
    cout<<"Enter the value of n1:";  /*'<<' is Insertion operator.*/
    cin>>n1;                           /*'>>' is Extraction operator.*/
    cout<<"Enter the value of n2:";  /*'<<' is Insertion operator.*/
    cin>>n2;                           /*'>>' is Extraction operator.*/
    cout<<"The sum is:"<<n1+n2;
    return 0;
}

// cin is used for taking input from user and
// cout is used for printing.